#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*l;

	l = NULL;
	while (*s != '\0')
	{
		if (*s == (char)c)
		{
			l = s;
		}
		s++;
	}
	if (c == '\0')
	{
		return ((char *)s);
	}
	return ((char *)l);
}
